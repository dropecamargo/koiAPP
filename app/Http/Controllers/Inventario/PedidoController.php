    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if ($request->ajax()) {
            $data = $request->all();

            $pedido = new Pedido1;
            if ($pedido->isValid($data)) {
                DB::beginTransaction();
                try {
                    // Marcas
                    $pedido->fill($data);
                    //$pedido->fillBoolean($data);
                    $pedido->pedido1_usuario_elaboro = Auth::user()->id;
                    $pedido->pedido1_fecha_elaboro = date('Y-m-d H:m:s');
                    $pedido->save();

                    // Commit Transaction
                    DB::commit();

                    return response()->json(['success' => true, 'id' => $pedido->id]);
                }catch(\Exception $e){
                    DB::rollback();
                    Log::error($e->getMessage());
                    return response()->json(['success' => false, 'errors' => trans('app.exception')]);
                }
            }
            return response()->json(['success' => false, 'errors' => $pedido->errors]);
        }
        abort(403);
    }

